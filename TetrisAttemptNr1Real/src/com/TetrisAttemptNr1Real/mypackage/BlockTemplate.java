package com.TetrisAttemptNr1Real.mypackage;

public class BlockTemplate {
	protected int x = 0;
	protected int y = 0;
	protected int flip = 0;
	public int getX() {
		return x;
	}
	public void setX(int pX) {
		this.x = pX;
	}
	public int getY() {
		return y;
	}
	public void setY(int pY) {
		this.y = pY;
	}
	public int getFlip() {
		return flip;
	}
	public void setFlip(int pFlip) {
		this.flip = pFlip;
	}
}
